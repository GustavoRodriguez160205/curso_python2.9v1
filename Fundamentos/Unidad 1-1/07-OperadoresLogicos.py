### Operadores Logicos

# AND 
## El operador AND es como esos días en los que para que algo salga bien, 
## todo tiene que alinearse. Si ambas condiciones son verdaderas,
## entonces funciona; si una falla, chau, no pasa.

resultado =  True & True # Devuelve True
resultado2 = False & True # Devuelve Falso
resultado3 = True & False # Devuelve Falso
resultado4 = False & False # Devuelve Falso

# OR
## Con OR, basta que una de las condiciones sea verdadera. 
## Es como cuando buscás excusas: con que una sea buena, listo, ya está.

resultado5 = True | False # Devuelve True
resultado6 = False| True # Devuelve True
resultado7 = True | False # Devuelve True
resultado8 = False | False # Devuelve Falso

# NOT

resultado9 = not True # Devuelve Falso
resultado10 = not False # Devuelve true

## NOT es simple: toma lo que tenés y lo invierte. 
## Lo que era True se vuelve False, y lo que era False se vuelve True. Es como cambiar de opinión.

