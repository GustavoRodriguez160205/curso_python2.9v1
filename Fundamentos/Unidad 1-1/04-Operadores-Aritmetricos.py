## Los operadores aritméticos son símbolos que se utilizan para realizar operaciones matemáticas

#######################################

## Suma y Resta ( + y  - )
suma = 12 + 5
resta = 12 - 5
print(f'El resultado de la suma es: {suma} y de la resta es: {resta}')

########################################

## Multi y División ( * y / )
multiplicar = 3 * 5
dividir = 2 / 4 
print(f'El resultado de la división es: {dividir} y de la multi es: {multiplicar}')

#########################################

## Potenciación (**)

exponente = 12 ** 5
print(f'El resultado de la potenciación es: {exponente}')

##########################################

## División baja: devuelve el entero de la división redondeado hacia abajo ( // )
divicion_baja = 12 // 5
print(f'El resultado de la divición baja es: {divicion_baja}')

############################################

## Resto o módulo

resto = 12 % 8
print(f'El resultado del resto es: {resto}')

